public class GettingIndividualDigits
	{
		public static void main(String[] args)
		{
		
		
			for  (int first = 1; first <=9; first++)
			{
			
				for (int second = 0; second < 9; second++)
				{
				System.out.println(first + "" + second + ", " + first + "+" + second + " = " + (first + second));
				}
			
			}
		}
	}